const I18nContext = createContext<I18nContextValue | null>(null);

function detectInitialLang(): { lang: Lang; chosen: boolean } {
  if (typeof window === "undefined") return { lang: "en", chosen: false };
  const stored = window.localStorage.getItem(STORAGE_KEY) as Lang | null;
  if (stored && dictionaries[stored]) return { lang: stored, chosen: true };
  const nav = (window.navigator.language || "en").slice(0, 2).toLowerCase();
  if (nav === "pt" || nav === "es" || nav === "fr") return { lang: nav as Lang, chosen: false };
  return { lang: "en", chosen: false };
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  const [hasChosen, setHasChosen] = useState(false);

  useEffect(() => {
    const { lang, chosen } = detectInitialLang();
    setLangState(lang);
    setHasChosen(chosen);
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    setHasChosen(true);
    try {
      window.localStorage.setItem(STORAGE_KEY, l);
      document.documentElement.lang = l;
    } catch {}
  }, []);

  const t = useCallback(
    (key: string) => {
      const dict = dictionaries[lang] ?? dictionaries.en;
      return dict[key] ?? dictionaries.en[key] ?? key;
    },
    [lang],
  );

  const value = useMemo(() => ({ lang, setLang, t, hasChosen }), [lang, setLang, t, hasChosen]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    return {
      lang: "en" as Lang,
      setLang: () => {},
      t: (key: string) => dictionaries.en[key] ?? key,
      hasChosen: false,
    };
  }
  return ctx;
}
